    public static boolean isIgnored(Block block) {
        return IGNORED_BLOCKS.contains(block);
    }

    private static void resolveBlock(String id) {
        try {
            Identifier identifier = Identifier.of(id);
            if (Registries.BLOCK.containsId(identifier)) {
                IGNORED_BLOCKS.add(Registries.BLOCK.get(identifier));
            }
        } catch (Exception e) {}
    }

    private static void resolveTag(String tagId) {
        try {
            Identifier identifier = Identifier.of(tagId);
            TagKey<Block> tagKey = TagKey.of(RegistryKeys.BLOCK, identifier);
            Registries.BLOCK.streamEntries()
                    .filter(entry -> entry.isIn(tagKey))
                    .forEach(entry -> IGNORED_BLOCKS.add(entry.value()));
        } catch (Exception e) {}
    }

    private static boolean isModLoaded(String modId) {
        return FabricLoader.getInstance().isModLoaded(modId);
    }

